    /**
     * Display a listing of the resource.
     *
     * @return Response
     */
    public function index()
    {
        //Get the auth validity
        if (Auth::check())
        {
            $userId = Auth::id();
            $roleGrant = User::where('id', '=', $userId)->first();
            
        if( $roleGrant->userrole_id == '1' && $roleGrant->userstatus_id == '1' ){
            //$collegetype = CollegeType::paginate(15);
            $collegetype = CollegeType::orderBy('collegetype.id', 'DESC')
                        ->leftJoin('users as eID','collegetype.employee_id', '=','eID.id')
                        ->paginate(15, array('collegetype.id', 'collegetype.name','eID.id as eUserId','eID.firstname as employeeFirstname', 'eID.middlename as employeeMiddlename', 'eID.lastname as employeeLastname','collegetype.updated_at'));
            return view('administrator/collegetype.index', compact('collegetype'));
        }else{
                Auth::logout(); // logout user
                return Redirect::to('login'); //redirect back to login
            }
        }else{
            Auth::logout(); // logout user
            return Redirect::to('login'); //redirect back to login
        }
    }

