    /**
     * Display a listing of the resource.
     *
     * @return Response
     */
    public function index()
    {
        //Get the auth validity
        if (Auth::check())
        {
            $userId = Auth::id();
            $roleGrant = User::where('id', '=', $userId)->first();
            
        if( $roleGrant->userrole_id == '1' && $roleGrant->userstatus_id == '1' ){
            //$addresstype = AddressType::orderBy('id', 'DESC')->paginate(15);
            $addresstype = AddressType::orderBy('addresstype.id', 'DESC')
                        ->leftJoin('users as eID','addresstype.employee_id', '=','eID.id')
                        ->paginate(15, array('addresstype.id', 'addresstype.name','eID.id as eUserId','eID.firstname as employeeFirstname', 'eID.middlename as employeeMiddlename', 'eID.lastname as employeeLastname','addresstype.updated_at'));

            return view('administrator/addresstype.index', compact('addresstype'));
        }else{
                Auth::logout(); // logout user
                return Redirect::to('login'); //redirect back to login
            }
        }else{
            Auth::logout(); // logout user
            return Redirect::to('login'); //redirect back to login
        }   
    }

