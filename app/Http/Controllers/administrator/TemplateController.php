    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\View\View
     */
    public function index(Request $request)
    {
        if(Auth::check()):
            if(Auth::user()->userrole_id == 4):
                $validateUserRoleCall = $this->fetchDataServiceController->validateUserRoleCall('Template');
                if((isset($validateUserRoleCall)) && (sizeof($validateUserRoleCall) > 0) && ($validateUserRoleCall[0]->index == '0')):
                    return Redirect::back();
                endif;
            endif;
        else:
            Auth::logout(); // logout user
            return Redirect::to('login'); //redirect back to login
        endif;

        if (Auth::check())
        {
            $userId = Auth::id();
            $roleGrant = User::where('id', '=', $userId)->first();
            
            if( $roleGrant->userrole_id == '1' || $roleGrant->userrole_id == '4' && $roleGrant->userstatus_id == '1' ){
                $keyword = $request->get('search');
                $perPage = 25;

                if (!empty($keyword)) {
                    $template = Template::where('templates.name', 'LIKE', "%$keyword%")
        				->orWhere('templates.description', 'LIKE', "%$keyword%")
        				->orWhere('templates.slug', 'LIKE', "%$keyword%")
        				->orWhere('templates.status', 'LIKE', "%$keyword%")
        				->paginate($perPage);
                } else {
                    $template = Template::paginate($perPage);
                }

                return view('/administrator/template.index', compact('template'));
            }else{
                Auth::logout(); // logout user
                return Redirect::to('login'); //redirect back to login
            }
        }else{
            Auth::logout(); // logout user
            return Redirect::to('login'); //redirect back to login
        }
    }

